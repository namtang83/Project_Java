    private void calculateluong_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateluong_btnActionPerformed
        String nguoiNhap = nguoinhap_tf.getText();
        java.util.Date ngayNhan = (java.util.Date) ngaynhan_jcal.getDate();
        String thuongThem_text = thuongthem_tf.getText();
        String maNV = manvcalluong_tf.getText();
        String hoTen = hotennv_lb.getText();
        String luongCB = luongcb_lb.getText();
        String heSL = hesoluong_lb.getText();
        
        if(maNV.isBlank() || !maNV.matches("A[0-9]{4}")){
            JOptionPane.showMessageDialog(null, "Mã nhân viên bắt đầu bằng chữ A, không để trống và có 5 ký tự!");
        } else if(hoTen.isBlank()){
            JOptionPane.showMessageDialog(null, "Họ tên trống! vui lòng nhập mã nhân viên!");
        } else if(luongCB.isBlank()){
            JOptionPane.showMessageDialog(null, "Lương cơ bản trống! vui lòng nhập mã nhân viên!");
        } else if(heSL.isBlank()){
            JOptionPane.showMessageDialog(null, "Hệ số lương trống! vui lòng nhập mã nhân ");
        } else if(nguoiNhap.isBlank() || !nguoiNhap.matches("[a-zA-Z\\s]+")){
            JOptionPane.showMessageDialog(null, "Người nhập không được để trống và là chuỗi!");
        } else if(ngayNhan == null){
            JOptionPane.showMessageDialog(null, "Ngày nhận không được để trống!");
        } else if(!thuongThem_text.matches("\\d+(\\.\\d+)?")){
            JOptionPane.showMessageDialog(null, "Thưởng thêm là số dương và không chứa ký tự!");
        } else {
            float luongcb = Float.parseFloat(luongcb_lb.getText());
            float hesoluong = Float.parseFloat(hesoluong_lb.getText());
            float thuongThem = Float.parseFloat(thuongthem_tf.getText());
            float tong = luongcb*hesoluong + thuongThem;
            String tongTien = String.valueOf(tong);
            tongtien_lb.setText(tongTien);
        }
    }//GEN-LAST:event_calculateluong_btnActionPerformed

