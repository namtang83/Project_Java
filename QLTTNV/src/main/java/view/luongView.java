    private void updateluong_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateluong_btnActionPerformed
        String manv = manvluong_tf.getText();
        String luongcb_text = luongnvluong_tf.getText();
        String hesoluong_text = hesoluongnvluong_tf.getText();
        if(manv.isBlank() || !manv.matches("A[0-9]{4}")){
            JOptionPane.showMessageDialog(null, "Mã nhân viên bắt đầu bằng chữ A và có 5 ký tự!","",JOptionPane.INFORMATION_MESSAGE);
        } else if(!luongcb_text.matches("\\d+(\\.\\d+)?")){
            JOptionPane.showMessageDialog(null, "Lương cơ bản phải lớn hơn không và không để trống!","",JOptionPane.INFORMATION_MESSAGE);
        } else if(!hesoluong_text.matches("\\d+(\\.\\d+)?")){
            JOptionPane.showMessageDialog(null, "Hệ số lương phải lớn hơn không và không để trống!","",JOptionPane.INFORMATION_MESSAGE);
        } else {
            int check = JOptionPane.showConfirmDialog(null,"Bạn có muốn cập nhật nhân viên này không?", "Cập nhật nhân viên", JOptionPane.YES_NO_OPTION);
            if(check == 0){
                float luongcb = Float.parseFloat(luongnvluong_tf.getText());
                float hesoluong = Float.parseFloat(hesoluongnvluong_tf.getText());
                NhanVien nv = new NhanVien();
                nv.setMaNV(manv);
                nv.setHeSoLuong(hesoluong);
                nv.setLuongCB(luongcb);
                NhanVienDAO nhanviendao = new NhanVienDAO();
                boolean kt = nhanviendao.checkMaNV(manv);
                if(kt){
                    System.out.println(kt);
                    nhanviendao.updateLuong(nv);
                } else {
                    JOptionPane.showMessageDialog(null, "Mã nhân viên không tồn tại!");
                    System.out.println(kt);
                }
                
            } 
        }
    }//GEN-LAST:event_updateluong_btnActionPerformed

